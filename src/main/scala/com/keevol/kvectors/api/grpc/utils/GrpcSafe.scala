import io.grpc.stub.StreamObserver

/**
 * wrap the routine of grpc into this util
 */
object GrpcSafe {

  def apply[T](responseObserver: StreamObserver[T])(resultCurator: => T): Unit = {
    try {
      responseObserver.onNext(resultCurator)
    } catch {
      case t: Throwable => responseObserver.onError(t)
    } finally {
      responseObserver.onCompleted()
    }
  }

}