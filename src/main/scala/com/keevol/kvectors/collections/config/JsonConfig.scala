import io.vertx.core.json.JsonObject
import org.apache.commons.io.FileUtils

import java.io.File
import java.nio.charset.StandardCharsets

object JsonConfig extends ConfigFeature {

  override def configFileSuffix: String = ".cfg.json"

  def apply(configFile: File): JsonObject = from(configFile)

  def from(configFile: File): JsonObject = {
    val content = FileUtils.readFileToString(configFile, StandardCharsets.UTF_8)
    new JsonObject(content)
  }

}